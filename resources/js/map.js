function setPage(){
	
	var preBtn = gridObj.filter(function (el){
        return el.PT_NO == spotId && el.PAGE == _pageNum + 1
    });
	
	var nextBtn = gridObj.filter(function (el){
        return el.PT_NO == spotId && el.PAGE == _pageNum - 1
    });
	
	
	if(preBtn.length > 0){
		$("#preBtn").show();
	}else{
		$("#preBtn").hide();
	}
	
	if(nextBtn.length > 0){
		$("#nextBtn").show();
	}else{
		$("#nextBtn").hide();
	}
}

